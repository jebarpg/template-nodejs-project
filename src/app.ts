/**
 * The function `sum` takes two parameters of any type and returns their sum.
 * @param {any} a - The function `sum` takes two parameters, `a` and `b`, of type `any`.
 * @param {any} b - The parameter `b` in the `sum` function represents the second value that you want
 * to add to the first value `a`.
 * @returns the sum of the two input parameters `a` and `b`.
 */
export default function sum(a: any, b: any) {
  return a + b;
}
